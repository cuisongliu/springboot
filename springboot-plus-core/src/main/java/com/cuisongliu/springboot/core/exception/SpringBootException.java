package com.cuisongliu.springboot.core.exception;

/**
 * 自定义异常
 *
 * @author cuisongliu [cuisongliu@qq.com]
 * @since 2017-10-23 11:26
 */
public class SpringBootException extends RuntimeException {

    private int code;
    private String message;

    public SpringBootException(int code, String message) {
        this.code = code;
        this.message = message;
    }
    public SpringBootException(ExceptionEnum plusExceptionEnum) {
        this(plusExceptionEnum.getCode(),plusExceptionEnum.getMessage());
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    @Override
    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
